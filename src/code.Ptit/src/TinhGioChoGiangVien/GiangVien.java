package TinhGioChoGiangVien;

public class GiangVien {
    private String magv, tengv;
    public GiangVien(String magv, String tengv){
        this.magv = magv;
        this.tengv = tengv;
    }
    public String getMagv() {
        return magv;
    }
    public String getTengv() {
        return tengv;
    }
}
