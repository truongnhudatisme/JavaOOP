package BangLuongTheoPhongBan;

public class PhongBan {
    private String map, tenp;

    public PhongBan(String map, String tenp) {
        this.map = map;
        this.tenp = tenp;
    }

    public String getMap() {
        return map;
    }

    public String getTenp() {
        return tenp;
    }
    
}
